class Vehicle {
  final String id;
  final String name;
  final String type;
  final String status;
  final String imageUrl;
  final double price;
  final String description;

  Vehicle({
    required this.id,
    required this.name,
    required this.type,
    required this.status,
    required this.imageUrl,
    required this.price,
    required this.description,
  });
}
